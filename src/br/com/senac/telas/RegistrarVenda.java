    private void botaoRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoRegistrarActionPerformed
        try{
            Integer quantidade;
            Double valor;
            Double custo;
            
            if (campoCusto.getText().isEmpty() || campoData.getText().isEmpty() || campoQnt.getText().isEmpty() || campoValor.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Preencha todos os campos!");
                return;
            }
            
            try {
                quantidade = Integer.valueOf(campoQnt.getText());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Quantidade inválida!");
                return;
            }
            
            try {
                String txtValor = campoValor.getText();
                String txtCusto = campoCusto.getText();
                txtValor = txtValor.replace(",", ".");
                txtCusto =  txtCusto.replace(",", ".");
                
                valor = Double.valueOf(txtValor);
                custo = Double.valueOf(txtCusto);
                
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Valor inválido em Valor e/ou Custo!");
                return;
            }
            
            SimpleDateFormat sdfData = new SimpleDateFormat("dd/MM/yyyy");
            sdfData.setLenient(false);
            Date data = sdfData.parse(campoData.getText());
            
            Vendas venda = new Vendas();
            
            venda.setQuantidade(quantidade);
            venda.setValor(valor);
            venda.setCusto(custo);
            venda.setData(data);
            
            String selecionado = comboAcoes.getSelectedItem().toString();
            String ticker = selecionado.split(" - ")[0];
            venda.setTickerAcao(ticker);
            
            VendasDAO vendasDao = new VendasDAO();
            
            if(vendasDao.isEmpty()){
                JOptionPane.showMessageDialog(this, "Erro de Conexão com o Banco de Dados!");
                return;
            }
            
            if(vendasDao.cadastrar(venda)){
                JOptionPane.showMessageDialog(this, "Dados inseridos com sucesso.");
                campoValor.setText("");
                campoQnt.setText("");
                campoData.setText("");
                campoCusto.setText("");
            }
            else{
                JOptionPane.showMessageDialog(this, "Erro para inserir os dados.");
            }
            
        }
        catch(java.text.ParseException e){
            JOptionPane.showMessageDialog(this, "Data inválida!");
        }
        
        
        
    }//GEN-LAST:event_botaoRegistrarActionPerformed

