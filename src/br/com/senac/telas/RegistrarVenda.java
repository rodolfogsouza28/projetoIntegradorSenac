    private void listarAcoes(){
        try {
            
            comboAcoes.removeAllItems();
            AcoesDAO acoesDao = new AcoesDAO();
            
            if(acoesDao.isEmpty()){
                JOptionPane.showMessageDialog(this, "Erro de Conexão com o Banco de Dados!");
                return;
            }
            
            
            
            List<Acoes> listagem = acoesDao.getAcoes();
            
            for(int i = 0; i < listagem.size(); i++){
                comboAcoes.addItem(listagem.get(i).getTicker() + " - " + listagem.get(i).getNome());
            }
            
        } catch (Exception e) {
        }
        
    }

