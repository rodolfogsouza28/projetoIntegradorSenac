    private void botaoBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoBuscarActionPerformed
        String ticker = campoTicker.getText();
        
        if (ticker.isEmpty()) {
            return;            
        }
        
               
        AcoesDAO acoesDao = new AcoesDAO();
        
        if(acoesDao.isEmpty()){
            JOptionPane.showMessageDialog(this, "Erro de Conexão com o Banco de Dados!");
            return;
        }
        
        Acoes acao = acoesDao.pesquisar(ticker);
        
        if(acao == null){
            JOptionPane.showMessageDialog(this, "Ação não encontrada!");
            campoNome.setText("");
            campoCnpj.setText("");
            campoTicker.setText("");
        }
        else{
            campoNome.setText(acao.getNome());
            campoCnpj.setText(acao.getCnpj());
            campoTicker.setText(acao.getTicker());
        }
        
        
    }//GEN-LAST:event_botaoBuscarActionPerformed

