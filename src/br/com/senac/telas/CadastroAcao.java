    private void botaoEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoEditarActionPerformed
        if(campoTicker.getText().isEmpty()){
            return;
        }
            
        if (campoNome.getText().isEmpty() || campoCnpj.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos!");
            return;
        }
            
        String ticker = campoTicker.getText();
        String nome = campoNome.getText();
        String cnpj = campoCnpj.getText();
            
            
            
        Acoes acao = new Acoes();
        acao.setNome(nome);
        acao.setCnpj(cnpj);
        acao.setTicker(ticker);
            
        AcoesDAO acoesDao = new AcoesDAO();
            
        if(acoesDao.isEmpty()){
            JOptionPane.showMessageDialog(this, "Erro de Conexão com o Banco de Dados!");
            return;
        }
            
        if(acoesDao.editar(acao)){
            JOptionPane.showMessageDialog(this, "Dados Atualizados com sucesso.");   
                
            campoNome.setText("");
            campoCnpj.setText("");
            campoTicker.setText("");
        }
        else{
            JOptionPane.showMessageDialog(this, "Erro em atualizar os dados.");
        }
    }//GEN-LAST:event_botaoEditarActionPerformed

    private void botaoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoExcluirActionPerformed
        String ticker = campoTicker.getText();
        
        if (ticker.isEmpty()) {
            return;            
        }
                     
        AcoesDAO acoesDao = new AcoesDAO();
        
        if(acoesDao.isEmpty()){
            JOptionPane.showMessageDialog(this, "Erro de Conexão com o Banco de Dados!");
            return;
        }
        
        if(acoesDao.excluir(ticker)){
            JOptionPane.showMessageDialog(this, "Dados excluídos com sucesso!");
            campoNome.setText("");
            campoCnpj.setText("");
            campoTicker.setText("");
        }
        else{
            JOptionPane.showMessageDialog(this, "Erro ao executar a exclusão, ação possui Compras/Vendas Cadastradas ou não existe!");
        }
    }//GEN-LAST:event_botaoExcluirActionPerformed

    private void botaoCadastarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCadastarActionPerformed

        if (campoNome.getText().isEmpty() || campoCnpj.getText().isEmpty() || campoTicker.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos!");
            return;
        }
            
        String ticker = campoTicker.getText();
        String nome = campoNome.getText();
        String cnpj  = campoCnpj.getText();
            
        Acoes acao = new Acoes();
        acao.setNome(nome);
        acao.setCnpj(cnpj);
        acao.setTicker(ticker);
            
        AcoesDAO acoesDao = new AcoesDAO();
            
        if(acoesDao.isEmpty()){
            JOptionPane.showMessageDialog(this, "Erro de Conexão com o Banco de Dados!");
            return;
        }
            
        if(acoesDao.cadastrar(acao)){
            JOptionPane.showMessageDialog(this, "Dados inseridos com sucesso.");   
                
            campoNome.setText("");
            campoCnpj.setText("");
            campoTicker.setText("");
        }
        else{
            JOptionPane.showMessageDialog(this, "Erro para inserir os dados.");
        }       
    }//GEN-LAST:event_botaoCadastarActionPerformed

